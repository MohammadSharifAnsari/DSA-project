typedef pair<int, int> P;
typedef pair<int, pair<int, int>> PP;
void dijkstraforCountMinStations(int source, vector<vector<int>> &adj, int destination, vector<string> &stations)
{
    priority_queue<P, vector<P>, greater<P>> pq;
    pq.push({0, source});

    int station = adj.size();
    vector<int> result(station, INT_MAX);
    result[source] = 0;

    while (!pq.empty())
    {
        int wt = pq.top().first;
        int u = pq.top().second;
        pq.pop();
        result[u] = wt;

        for (int v = 0; v < station; v++)
        {
            if (adj[u][v])
            {
                if (result[v] > wt + 1)
                {
                    result[v] = wt + 1;
                    pq.push({wt + 1, v});
                }
            }
        }
    }

    for (int i = 0; i < station; i++)
    {
        if (i == destination)
            cout << "Min stations between " << stations[source] << " and " << stations[i] << " " << result[i] << endl;
    }
}

void dijkstraforPrintsStations(int source, int destination, vector<vector<int>> &adj, vector<string> &stations, vector<bool> &changeTrain)
{
    int station = adj.size(); // No of stations
    vector<int> result(station, INT_MAX);
    vector<int> parent(station);

    for (int i = 0; i < station; i++)
    {
        parent[i] = i;
    }

    priority_queue<PP, vector<PP>, greater<PP>> pq;
    pq.push({0, {source, source}});
    result[source] = 0;

    while (!pq.empty())
    {
        int wt = pq.top().first;
        int u = pq.top().second.first;
        int par = pq.top().second.second;
        pq.pop();

        result[u] = wt;
        parent[u] = par;

        for (int v = 0; v < station; v++)
        {
            if (adj[u][v] && result[v] > wt + adj[u][v])
            {
                result[v] = wt + adj[u][v];
                parent[v] = u;
                pq.push({wt + adj[u][v], {v, u}});
            }
        }
    }

    vector<string> listofStations;
    int index = destination;
    listofStations.push_back(stations[destination]);
    while (parent[index] != index)
    {
        listofStations.push_back(stations[parent[index]]);
        index = parent[index];
    }

    reverse(listofStations.begin(), listofStations.end());
    cout << "List of stations between " << stations[source] << " and " << stations[destination] << endl;

    unordered_map<string, bool> mp;
    for (int i = 0; i < station; i++)
    {
        if (changeTrain[i])
            mp[stations[i]] = true;
        else
            mp[stations[i]] = false;
    }
    int n = listofStations.size();
    for (int i = 0; i < n; i++)
    {
        if (listofStations[i] != stations[source] && listofStations[i] != stations[destination] && mp[listofStations[i]])
        {
            cout << listofStations[i] << endl;
            cout << " " << endl;
            cout << "change train" << endl;
            cout << " " << endl;
        }
        cout << listofStations[i] << endl;
    }
}
